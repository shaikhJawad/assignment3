package assignment3;

public class Student {
	
	int id;
	String name;
	String grade;
	int age;
	public void Student()
	{
		
	}
	
	public void setId(int Id)
	{
		this.id = Id;
	}
	
	public int getId()
	{
		return this.id;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return this.name;
	}
	
	public void setGrade(String grade)
	{
		this.grade = grade;
	}
	
	public String getGrade()
	{
		return this.grade;
	}
	public void setAge(int age)
	{
		this.age = age;
	}
	
	public int getAge()
	{
		return this.age;
	}
	
}
